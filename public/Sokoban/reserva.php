<p> En 1992 el quinto ángel tocó la trompeta, y vi los 16
 bits cayendo del Corte Inglés a la tierra; y se les
 dio la llave del mercado. Y acapararon el mercado; y
 se oscureció el software español y el Spectrum. Y del
 Corte Inglés salieron Amigas, Compatibles y Super
 Nintendos sobre la tierra; y se les dio poder, como
 tienen poder los sistemas de nueva generación.</p>

<p> Y en aquellos días los Spectrum buscaron la muerte,
 pero no la hallaron; y ansiaron morir, pero la muerte
 huyó de ellos dejándolos en manos de la emulación.</p>

