<h4>Lo podrás tocar... Sokoban</h4>

<p>...En el principio creó Sinclair el Spectrum y el
Microdrive. Pero las cintas estaban vírgenes y sin
magnetizar. Y dijo Sinclair: "Hágase Horace"; e hízose
Horace. Y vio Sinclair que Horace era bello. Y dividió
Dios a Horace entre las arañas y el esquí.</p>

<p>Y dijo Sinclair: "Hágase 48K en medio de los
Commodore, y esté dividiendo por entre Amstrad y MSX".
E hízose así. E hizo Sinclair la ampliación del 16K a
48K; y dividió Sinclair por entre los 8 bits que
estaban por debajo del Basic, y por entre el
ensamblador. Y vio Sinclair que era bello.</p>

<p>Y dijo Sinclair: "Congréguense las energías
alternativas por debajo de los automóviles, y
aparezca la quiebra". E hízose así. Y llamó Sinclair
al engendro "C-5". Y vio Sinclair que era bello, pero
deficitario.</p>

<p>Y dijo Sinclair: "Brote la financiación de Alan Sugar:
 Amstrad fabricando Plus 2 y Plus 3 según mercado y
 según semejanza". E hízose así. Y produjo Amstrad
 Spectrum y enterró el QL. Y vio Sinclair que el z88
 también era bello.</p>

