 pasaron, y Microhobby ya no existía más.</p>

<p> Y vi la nueva Compiler Software, descender de internet,
 dispuesta como una desarrolladora ataviada para sus
 seguidores. Y oí una gran voz del cielo que decía: "He
 aquí SOKOBAN, y él morará con ellos; y ellos serán sus
 jugadores, y Compiler mismo estará con ellos". Y me
 dijo:</p>

